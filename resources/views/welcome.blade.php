                $.ajax({
                    method: 'post',
                    url: '{{route('geolocation.do.add')}}',
                    dataType: 'json',
                    data: {
                        latitude: position.coords.latitude,
                        longitude: position.coords.longitude,
                        _token: '{{csrf_token()}}'
                    },
                    success: function (res) {
                        console.log(res);
                    },
                    error: function (err) {
                        console.log(err);
                    }
                });
            });
        } else {
            console.log("Geolocation is not supported by this browser.");
            this.loaderLeave();
        }
    });
